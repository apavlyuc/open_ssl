#include "../inc/libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;

	i = 0;
	if (!f || !s)
		return ;
	while (s[i] != '\0')
	{
		f(&s[i]);
		i++;
	}
}
